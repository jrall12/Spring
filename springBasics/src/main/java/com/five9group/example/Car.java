package com.five9group.example;

public class Car {

	private String nameOfCar;
	private int perDayCost;
	
	public final String getNameOfCar() {
		return nameOfCar;
	}
	public final void setNameOfCar(final String nameOfCar) {
		this.nameOfCar = nameOfCar;
	}
	public final int getPerDayCost() {
		return perDayCost;
	}
	public final void setPerDayCost(final int perDayCost) {
		this.perDayCost = perDayCost;
	}
	
	
}
